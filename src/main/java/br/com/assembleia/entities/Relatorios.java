package br.com.assembleia.entities;

public class Relatorios {

    private String descricaoLink;
    private String linkRelatorio;
    private String descricaoRelatorio;

    public String getDescricaoLink() {
        return descricaoLink;
    }

    public void setDescricaoLink(String descricaoLink) {
        this.descricaoLink = descricaoLink;
    }

    public String getLinkRelatorio() {
        return linkRelatorio;
    }

    public void setLinkRelatorio(String linkRelatorio) {
        this.linkRelatorio = linkRelatorio;
    }

    public String getDescricaoRelatorio() {
        return descricaoRelatorio;
    }

    public void setDescricaoRelatorio(String descricaoRelatorio) {
        this.descricaoRelatorio = descricaoRelatorio;
    }

}
